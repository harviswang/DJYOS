{
	pg_sysctrl_reg->UART1CLKDIV = (dev_num & 0xFF);
}

//----设置io滤波采样时钟除数0--------------------------------------------------
//功能: lpc12xx有7个时钟用于io口输入抗干扰滤波，每个时钟从主频分频，分频数由本组
//      函数配置，每个io口可以独立选择其中一个时钟，0=禁止该时钟
//参数: dev_num
//返回: 无
//-----------------------------------------------------------------------------
