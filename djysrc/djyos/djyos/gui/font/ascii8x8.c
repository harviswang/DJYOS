//----点阵提取-----------------------------------------------------------------
//功能: 提取ascii 8*8点阵字体，如果charcode超出0~0xff的范围，显示编码为0x00的字
//      符，ascii包含扩展的ascii码
//参数: charcode，待显示的ascii码得ucs4编码
//      size，无效
//      resv，无效
//      bitmap，保存所提取的点阵的位图，缓冲区由调用者提供
//返回: true=正常，false=charcode不是该字体所支持的字符集范围，但此时仍然返回
//      默认字符的点阵
//-----------------------------------------------------------------------------
bool_t __ascii8x8_get_char_bitmap(u32 charcode, u32 size,u32 resv,
                                    struct st_rect_bitmap *bitmap)
{
    u8 i;
    u32 offset;
    bool_t result = true;


    // 得到字符字模在数组中的偏移量
    if(charcode > 0xff)
    {
        offset = ASCII8x8_DEF_CHAR_CODE;
        result = false;
    }else
        offset = charcode*8;
    bitmap->pf_type = cn_sys_pf_gray1;
    bitmap->width = 8;
    bitmap->height = 8;
    bitmap->linebytes = 1;
    bitmap->pf_type = cn_sys_pf_gray1;
    if(bitmap->bm_bits != NULL)
    {
        for(i=0; i<8; i++)
        {
            bitmap->bm_bits[i] = cs_ascii_8x8[offset + i]; 
        }
    }

    return result;
}


//----安装ascii8x8字体-------------------------------------------------------
//功能: 安装ascii字体，当系统使用西方字符界面时，使用这个字符集。特别注意，
//      gb2312已经包含了英文字体，在使用中文的界面中可以不安装ascii字体。
//参数: 无意义
//返回: 1=成功，0=失败
//-----------------------------------------------------------------------------
ptu32_t module_init_font_ascii_8x8(ptu32_t para)
{
    static struct font_rsc font_a8x8;

    font_a8x8.fi.type = cn_font_type_dot;
    font_a8x8.load_font = __ascii8x8_load_font;
    font_a8x8.unload_font = __ascii8x8_unload_font;
    font_a8x8.get_char_bitmap = __ascii8x8_get_char_bitmap;
    
    if(font_install_font(&font_a8x8, cn_font_ascii_8x8))
    {
    	printf_ng("ascii 8x8 font install sucess\n\r");
        return 1;
    }else
    {
        djy_error_login(enum_gk_font_install_error,
                        "ascii 8x8 font install fail");
        return 0;
    }
}

